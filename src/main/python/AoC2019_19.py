Cell = tuple[int, int]


class Beam(NamedTuple):
    program: list[int]

    @classmethod
    def from_string(cls, string: str) -> Beam:
        return Beam([int(_) for _ in string.split(",")])

    def contains(self, cell: Cell) -> bool:
        row, col = cell
        out = list[int]()
        IntCode(self.program).run([row, col], out)
        return out[0] == 1


