class Solution(SolutionBase[Input, Output1, Output2]):
    def parse_input(self, input_data: InputData) -> Input:
        return Beam.from_string(list(input_data)[0])

    def part_1(self, beam: Input) -> Output1:
        return self.solve_1(beam, 50)

    def solve_1(self, beam: Input, size: int) -> int:
        return sum(
            beam.contains((r, c)) for c in range(size) for r in range(size)
        )

    def part_2(self, beam: Input) -> Output2:
        return self.solve_2(beam, 100)

    def solve_2(self, beam: Input, size: int) -> int:
        r, c = size - 1, 0
        while True:
            while not beam.contains((r, c)):
                c += 1
            if beam.contains((r - (size - 1), c + (size - 1))):
                return (r - (size - 1)) * 10_000 + c
            r += 1

    def samples(self) -> None:
        puzzle = aocd.models.Puzzle(self.year, self.day)
        beam = self.parse_input(my_aocd.get_input_data(puzzle))
        for line in [
            "".join("#" if beam.contains((r, c)) else "." for c in range(50))
            for r in range(50)
        ]:
            log(line)
        assert self.solve_1(self.parse_input(TEST1.splitlines()), 10) == 27
        assert self.solve_2(self.parse_input(TEST2.splitlines()), 10) == 250020


